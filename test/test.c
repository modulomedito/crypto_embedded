#define TEST_CONCAT_HELPER(a, b) a##b
#define TEST_CONCAT(a, b) TEST_CONCAT_HELPER(a, b)

#define TEST_RUN(mod)                                                          \
    do {                                                                       \
        extern int32_t TEST_CONCAT(mod, _test)(void);                          \
        int32_t result = TEST_CONCAT(mod, _test)();                            \
        if (result != 0) {                                                     \
            printf("Test " #mod " FAILED! (Failed at line %d)\n", result);     \
            test_failed_cnt++;                                                 \
        }                                                                      \
    } while (0)

